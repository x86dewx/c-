#include<stdio.h>

int main(void)
{
    int a[][3] ={1,2,3,4,5,6,7,8,9};
    int m,n;
    m = a[0][0] * a[1][1] * a[2][2] + a[0][1] * a[1][2] * a[2][0] + a[1][0] * a[2][1] * a[0][2];
    n = a[0][2] * a[1][1] * a[2][0] + a[0][1] * a[1][0] * a[2][2] + a[1][2] * a[2][1] * a[0][0];
    printf("行列式的值为%d\n",m - n);
}
